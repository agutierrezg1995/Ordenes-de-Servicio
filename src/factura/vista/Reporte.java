    /**
     * Método realizarConsulta.
     * Consulta las facturas en la base de datos según el rango de fechas proporcionado.
     * Los resultados se muestran en la tabla interactiva.
     */
    private void realizarConsulta() {
        System.out.println("Ejecutando consulta con parámetros:");
        System.out.println("Fecha inicio: " + txtFechaInicio.getText());
        System.out.println("Fecha fin: " + txtFechaFin.getText());

        // Consulta SQL para obtener las facturas dentro del rango de fechas
        String sql = "SELECT numeroFactura, fecha, idCliente, observaciones, total FROM facturas WHERE fecha BETWEEN ? AND ?";
        System.out.println("Consulta SQL: " + sql);
        try (Connection conexion = ConexionBD.obtenerConexion(); PreparedStatement ps = conexion.prepareStatement(sql)) {
            ps.setString(1, txtFechaInicio.getText()); // Establece la fecha de inicio como parámetro
            ps.setString(2, txtFechaFin.getText()); // Establece la fecha de fin como parámetro

            ResultSet rs = ps.executeQuery(); // Ejecuta la consulta y obtiene los resultados
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tablaReporte.getModel();
            model.setRowCount(0); // Limpia la tabla antes de mostrar nuevos resultados

            while (rs.next()) {
                // Agrega cada fila de resultados a la tabla
                model.addRow(new Object[]{
                    rs.getString("numeroFactura"),
                    rs.getString("fecha"),
                    rs.getInt("idCliente"),
                    rs.getString("observaciones"),
                    rs.getDouble("total")
                });
            }
        } catch (SQLException e) {
            e.printStackTrace(); // Imprime la traza de la excepción en caso de error
        }
    }

    /**
     * Método configurarTablaClickListener.
     * Configura un evento de clic en la tabla para actualizar los campos de texto y el combo box
     * con la información de la factura seleccionada.
     */
    private void configurarTablaClickListener() {
        tablaReporte.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int selectedRow = tablaReporte.getSelectedRow(); // Obtiene la fila seleccionada
                if (selectedRow != -1) {
                    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tablaReporte.getModel();
                    txtFactura.setText(model.getValueAt(selectedRow, 0).toString()); // Actualiza el campo de texto con el número de factura

                    // Consulta para obtener el nombre del cliente según el ID
                    String idCliente = model.getValueAt(selectedRow, 2).toString();
                    String sqlCliente = "SELECT nombre FROM clientes WHERE idCliente = ?";
                    try (Connection conexion = ConexionBD.obtenerConexion(); PreparedStatement ps = conexion.prepareStatement(sqlCliente)) {
                        ps.setString(1, idCliente); // Establece el ID del cliente como parámetro
                        ResultSet rs = ps.executeQuery(); // Ejecuta la consulta
                        if (rs.next()) {
                            cbxClienteReporte.setSelectedItem(rs.getString("nombre")); // Actualiza el combo box con el nombre del cliente
                        }
                    } catch (SQLException e) {
                        e.printStackTrace(); // Imprime la traza de la excepción en caso de error
                    }
                }
            }
        });
    }

    /**
     * Método cargarClientes.
     * Carga los nombres de los clientes desde la base de datos y los agrega al combo box.
     */
    private void cargarClientes() {
        String sql = "SELECT idCliente, nombre FROM clientes"; // Consulta SQL para obtener los clientes
        try (Connection conexion = ConexionBD.obtenerConexion(); PreparedStatement ps = conexion.prepareStatement(sql)) {
            ResultSet rs = ps.executeQuery(); // Ejecuta la consulta y obtiene los resultados
            cbxClienteReporte.removeAllItems(); // Limpia el combo box antes de agregar nuevos elementos
            while (rs.next()) {
                cbxClienteReporte.addItem(rs.getString("nombre")); // Agrega cada cliente al combo box
            }
        } catch (SQLException e) {
            e.printStackTrace(); // Imprime la traza de la excepción en caso de error
        }
    }

