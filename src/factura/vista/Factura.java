    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try (Connection conexion = ConexionBD.obtenerConexion()) {
            // Consultar los datos del producto desde la base de datos
            String sql = "SELECT codigo, nombre, precio FROM productos WHERE nombre = ?";
            try (PreparedStatement ps = conexion.prepareStatement(sql)) {
                ps.setString(1, cbxProducto.getSelectedItem().toString());
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        // Obtener los datos del producto
                        String codigo = rs.getString("codigo");
                        String nombre = rs.getString("nombre");
                        double precio = rs.getDouble("precio");

                        // Calcular el subtotal (cantidad por precio)
                        int cantidad = 1; // Por defecto, agregar 1 unidad
                        double subtotal = precio * cantidad;

                        // Agregar una nueva fila al inicio de la tabla
                        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tablaFactura.getModel();
                        model.insertRow(0, new Object[]{codigo, nombre, precio, cantidad, subtotal});

                        // Actualizar el total
                        double totalActual = txtTotal.getText().isEmpty() ? 0 : Double.parseDouble(txtTotal.getText());
                        totalActual += subtotal;
                        txtTotal.setText(String.valueOf(totalActual));
                    }
                }
            } catch (SQLException e) {
                System.err.println("Error en la base de datos: " + e.getMessage());
            }
        } catch (SQLException e) {
            System.err.println("Error al agregar el producto: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try (Connection conexion = ConexionBD.obtenerConexion()) {
            // Obtener el idCliente basado en el nombre seleccionado en el combo box
            String clienteSeleccionado = cbxClienteFactura.getSelectedItem().toString();
            int idCliente = -1;
            String sqlCliente = "SELECT idCliente FROM clientes WHERE nombre = ?";
            try (PreparedStatement psCliente = conexion.prepareStatement(sqlCliente)) {
                psCliente.setString(1, clienteSeleccionado);
                try (ResultSet rsCliente = psCliente.executeQuery()) {
                    if (rsCliente.next()) {
                        idCliente = rsCliente.getInt("idCliente");
                    }
                }
            }

            if (idCliente == -1) {
                throw new SQLException("No se encontró el cliente seleccionado en la base de datos.");
            }

            // Insertar la factura en la base de datos
            String sqlFactura = "INSERT INTO facturas (idFactura, numeroFactura, fecha, idCliente, observaciones, total) VALUES (?, ?, ?, ?, ?, ?)";
            try (PreparedStatement psFactura = conexion.prepareStatement(sqlFactura)) {
                psFactura.setInt(1, obtenerNuevoIdFactura()); // Método para obtener el nuevo ID
                psFactura.setString(2, txtNroFactura.getText());
                psFactura.setString(3, txtFecha.getText());
                psFactura.setInt(4, idCliente); // Usar el idCliente obtenido
                psFactura.setString(5, txtObservaciones.getText());
                psFactura.setDouble(6, Double.parseDouble(txtTotal.getText()));
                psFactura.executeUpdate();
            }

            // Limpiar el formulario y la tabla
            txtNroFactura.setText("");
            txtFecha.setText("");
            cbxClienteFactura.setSelectedIndex(0);
            txtObservaciones.setText("");
            txtTotal.setText("");
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tablaFactura.getModel();
            model.setRowCount(0);

            System.out.println("Factura registrada correctamente.");
        } catch (SQLException e) {
            System.err.println("Error en la base de datos: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

