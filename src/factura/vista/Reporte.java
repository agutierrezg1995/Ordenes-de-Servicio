    /**
     * Método que se ejecuta al presionar el botón "Exportar"
     */
    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
                String numeroFactura = txtFactura.getText(); // Obtiene el número de factura del campo de texto
                if (numeroFactura.isEmpty()) {
                    System.out.println("No se ha seleccionado ninguna factura para exportar."); // Mensaje en consola si no hay factura seleccionada
                    javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione una factura antes de exportar."); // Mensaje en la interfaz
                    return;
                }

                String sql = "SELECT numeroFactura, fecha, idCliente, observaciones, total FROM facturas WHERE numeroFactura = ?"; // Consulta SQL para obtener los datos de la factura
                try (Connection conexion = ConexionBD.obtenerConexion(); PreparedStatement ps = conexion.prepareStatement(sql)) {
                    ps.setString(1, numeroFactura); // Establece el número de factura como parámetro
                    ResultSet rs = ps.executeQuery(); // Ejecuta la consulta

                    if (rs.next()) {
                        // Crea el contenido del archivo TXT con los datos de la factura
                        String contenido = "Factura: " + rs.getString("numeroFactura") + "\n" +
                                          "Fecha: " + rs.getString("fecha") + "\n" +
                                          "Cliente: " + rs.getInt("idCliente") + "\n" +
                                          "Observaciones: " + rs.getString("observaciones") + "\n" +
                                          "Total: " + rs.getDouble("total") + "\n";

                        java.nio.file.Path path = java.nio.file.Paths.get("factura_" + numeroFactura + ".txt"); // Define la ruta del archivo
                        java.nio.file.Files.write(path, contenido.getBytes()); // Escribe el contenido en el archivo
                        System.out.println("Factura exportada exitosamente: " + path.toAbsolutePath()); // Mensaje en consola indicando éxito
                        javax.swing.JOptionPane.showMessageDialog(this, "Factura exportada exitosamente."); // Mensaje en la interfaz

                        java.awt.Desktop.getDesktop().open(path.toFile()); // Abre el archivo automáticamente
                    } else {
                        System.out.println("No se encontraron datos para la factura: " + numeroFactura); // Mensaje en consola si no se encuentran datos
                        javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron datos para la factura seleccionada."); // Mensaje en la interfaz
                    }
                } catch (SQLException | java.io.IOException e) {
                    e.printStackTrace(); // Imprime la traza de la excepción en caso de error
                    System.out.println("Error al exportar la factura: " + e.getMessage()); // Mensaje en consola indicando error
                    javax.swing.JOptionPane.showMessageDialog(this, "Error al exportar la factura."); // Mensaje en la interfaz
                }
    }//GEN-LAST:event_btnExportarActionPerformed

