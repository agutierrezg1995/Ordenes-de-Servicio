    private int obtenerNuevoIdFactura() {
        int nuevoId = 1; // Valor por defecto en caso de que no se encuentre ningún registro
        try (Connection conexion = ConexionBD.obtenerConexion()) {
            String sql = "SELECT MAX(idFactura) AS maxId FROM facturas";
            try (PreparedStatement ps = conexion.prepareStatement(sql);
                 ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    nuevoId = rs.getInt("maxId") + 1;
                }
            }
        } catch (SQLException e) {
            System.err.println("Error al obtener el nuevo ID de factura: " + e.getMessage());
        }
        return nuevoId;
    }
